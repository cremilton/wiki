</code>
</pre>
                <p>Desativando no Windows 10:</p>
<pre>
<code class="language-powershell">
bcdedit /set recoveryenabled NO
</code>
</pre>
                <p>Ativando no Windows 7:</p>
<pre>
<code class="language-powershell">
bcdedit /set {default} bootstatuspolicy displayallfailures
</code>
</pre>
                <p>Ativando no Windows 10:</p>
<pre>
<code class="language-powershell">
bcdedit /set recoveryenabled YES
</code>
</pre>                

                <p>Isso evita o reparo automático do SO em caso de perda de energia. O que pode quebrar o congelamento do SO.</p>

