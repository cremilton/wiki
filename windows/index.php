                <h4 id="var">Variáveis do Sistema&nbsp;<span class="installed">All lab</span></h4>
                <blockquote>
                Crie as variáveis para a última versão do Java JDK instalado. No meu caso, possuo duas instalações, o JDK7 por causa do IReport, e o JDK 8 para os programas que necessitam de uma versão mais recente.
                </blockquote>
                <p>Acesse as <b>Propriedades do Sistema</b> para configurar as variáveis. Neste exemplo, estou configurando o <b>JDK 1.8.0_191</b>.</p>
                <img src="../imagens/windows/variavel-java.png" alt="Propriedades do Sistema">
<pre>
<code class="language-powershell">
JAVA_HOME
C:\Program Files\Java\jdk1.8.0_191
</code>
</pre>
                <p>A variável <code class="inline">Path</code> já existe no sistema, então apenas adicione o valor ao final!</p>
                <p>No Windows 7:</p>
<pre>
<code class="language-powershell">
Path
;%JAVA_HOME%\bin
</code>
</pre>
                <p>No Windows 10:</p>
<pre>
<code class="language-powershell">
Path
%JAVA_HOME%\bin
</code>
</pre>
                <h3>Variáveis via cmd</h3>
<pre>
<code class="language-powershell">
setx JAVA_HOME "C:\Program Files\Java\jdk1.8.0_191" /m
setx Path "%Path%;%JAVA_HOME%\bin" /m
</code>
</pre>                
